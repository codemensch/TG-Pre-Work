let sale = true;

sale = false;

if(sale) {
  console.log('Time to buy!');
}
else {
  console.log('Time to wait for a sale.');
}

/*If we want to execute code when the condition returns false, an else statement is
used in conjunction with the if statement. An else statement requires an if statement.
Like the if statement, code inside the curly braces of the else statement will run,
but this time when the condition is false.*/
