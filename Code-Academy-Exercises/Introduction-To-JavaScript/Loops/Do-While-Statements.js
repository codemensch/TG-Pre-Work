let cupsOfSugarNeeded = 2;
let cupsAdded = 0;

do{
  cupsAdded++;
}
while(cupsAdded < cupsOfSugarNeeded);

/*If it is required that code in the loop needs to run at least once, we can
use the do..while loop. The do in the do..while loop takes the loop code in
curly braces and will immediately run once. The while in the do..while is after
the do statement and contains the stopping condition, continuing to run the
do statement until the condition is no longer true.*/
